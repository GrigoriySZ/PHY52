-- Операторы сравнение:
-- > - больше
-- < - меньше
-- >= - больше или равно
-- <= - меньше или равно
-- = - равно
-- <> - не равно

-- Логические операторы:
-- and - логическое И
-- or - логическое ИЛИ
-- not - логическое не отрицание
-- BETWEEN - оператор МЕЖДУ (границы диапазона включены в выборку)
-- (WHERE weight >= 20 AND weight <= 100) равно (WHERE weight BETWEEN 25 AND 100);
-- IN - оператор В (a, b, c) - Значения в скобках перечилсяются через OR
-- (WHERE weight = 20 OR weight = 100 OR weight = 150) равно (WHERE weight IN (25, 100, 150));

