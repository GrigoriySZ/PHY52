DDL (data definition language)
БД, таблицы, триггеры, процедуры
триггер - готовый код, который будет реагировать при определённых действиях
процедуры - готовый (скомпилированный) код
3 базовые операции DDL:
    1. CREATE - создать
    2. ALTER - изменить
    3. DROP - полное удаление

DML (data manipulation language)
Операторвы, которые манипулируют данными:
CRUD:
    Create     INSERT INTO
    Read       SELECT
    Update     UPDATE
    Delete     DELETE
Операторы, которые отвечают за создание

DTL (data transaction language)

